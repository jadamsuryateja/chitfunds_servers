const seedDistricts = async () => {
    try {
        await connectDB();
        console.log('DB Connected - Seeding Districts...');

        for (const data of indiaData) {
            // Find or Create State
            let state = await State.findOne({ name: data.state });
            if (!state) {
                state = await State.create({ name: data.state, code: data.code, isActive: true });
                console.log(`Created State: ${data.state}`);
            } else {
                console.log(`State exists: ${data.state}`);
            }

            // Create Districts
            for (const distName of data.districts) {
                const districtExists = await District.findOne({ name: distName, state: state._id });
                if (!districtExists) {
                    // Create a unique slug: district-statecode (e.g., bilaspur-hp)
                    const slug = `${distName.toLowerCase().replace(/ /g, '-')}-${data.code.toLowerCase()}`;

                    try {
                        await District.create({
                            name: distName,
                            state: state._id,
                            isActive: true,
                            slug: slug
                        });
                        // console.log(`  + Added District: ${distName}`);
                    } catch (err) {
                        console.error(`Failed to create district ${distName} in ${data.state}: ${err.message}`);
                    }
                }
            }
            console.log(`  > Processed ${data.districts.length} districts for ${data.state}`);
        }

        console.log('Seed Completed Successfully');
        process.exit();
    } catch (error) {
        console.error('Seed Error:', error);
        process.exit(1);
    }
};

seedDistricts();
